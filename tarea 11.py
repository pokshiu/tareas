lista_numeros = [1, 5, 8, 12, 3]
suma_total = 0

# El bucle 'for' itera sobre cada elemento de la lista.
for numero in lista_numeros:
    suma_total += numero # Acumula el valor de cada número

print("\n11. Suma de Elementos con Bucle for ")
print(f"La lista de números es: {lista_numeros}")
print(f"La suma de todos sus elementos es: {suma_total}")